// ─── Helper to make message creation less verbose ──────────────────

let msgCounter = 0

function makeMsg(
  overrides: Partial<ChorusMessage> & Pick<ChorusMessage, 'conversationId' | 'conversationType' | 'senderId' | 'senderName' | 'content' | 'timestamp'>
): ChorusMessage {
  msgCounter++
  return {
    id: `msg-${msgCounter}`,
    senderAvatarUrl: '',
    messageType: ChorusMessageType.Text,
    editedAt: null,
    isEdited: false,
    threadId: null,
    threadReplyCount: 0,
    threadParticipantIds: [],
    threadLastReplyAt: null,
    reactions: [],
    isPinned: false,
    isBookmarked: false,
    isDeleted: false,
    attachments: [],
    mentions: [],
    pollData: null,
    crossModuleRef: null,
    ...overrides,
  }
}

